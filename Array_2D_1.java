public class Array_2D_1 {

	  public static void main (String args []) {

		    int [][] num = new int [2][3];
		    
		    num [0][0] = 10;
		    num [0][1] = 20;
		    num [0][2] = 4;
		    num [1][0] = 7;
		    num [1][1] = 8;
		    num [1][2] = 9;
		    
		    System.out.println(num [0][0]);
		    System.out.println(num [1][0]);
		    System.out.println(num [1][1]);
	  }
}
	  